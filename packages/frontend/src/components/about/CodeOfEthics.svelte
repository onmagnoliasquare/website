<div class="mw6">
	<h2 class="f2 f1-l ma0 pa0 fw6 tracked-tight-2">Code of Ethics</h2>
	<div class="">
		<div class="ba border-color pa2">
			<ol class="tracked-02 lh-copy pr2">
				<li>Ethical journalism should be accurate, fair, and balanced.</li>
				<li>All journalists conduct themselves with integrity.</li>
				<li>
					Journalists should be honest and orderly in gathering, reporting, and interpreting
					information.
				</li>
				<li>
					Journalists will not attempt to create unnecessary panic or confusion through their
					articles.
				</li>
				<li>
					All journalists treat their sources and colleagues with professionalism and courtesy.
				</li>
				<li>
					The highest and primary obligation of ethical journalism is to serve the community at NYU
					Shanghai
				</li>
				<li>
					All journalists take responsibility for their work and attach their names to their writing
					completely.
				</li>
				<li>
					All opinions published reflect the journalist's opinions and not those of the OMS
					publication.
				</li>
				<li>
					Non-English articles are translated to the best of our ability, and we strive to maintain
					the original language's meaning and intent.
				</li>
				<li>
					We do not tolerate plagiarism and any writer caught plagiarizing will be immediately
					removed from the staff.
				</li>
				<li>
					We do not accept anonymous sources and attribution. All attribution must include full
					names of sources.
				</li>
			</ol>
		</div>
	</div>
</div>
